public class java_output {
    public static void main(String[] args) {
        
        // output print vs println and \n
        System.out.println("C++ Is Great"); // C++ is Great printed on one line
        System.out.print("Java is "); // Java is Better printed on one line
        System.out.print("Better\n"); // \n is used to print a new line. Take out the \n in this code to see what happens
        
        // variables and literals
        Double pi = 3.14159265359;
        System.out.println("pi = " + pi); // pi is a variable
        System.out.println("golden ration = " + 1.61803398875); // 1.61803398875 is a numeric literal

        // concatenated strings
        System.out.println("Morphy," + "Paul Morphy");

    }
}