public class Integer_Arithmetic{
 public static void main(String[] args){
 int x = 2;
 int y = 1;
 int result = 0;
 
 result = x + y;
 System.out.println(result); // 3
 
 result = x - y;
 System.out.println(result); // 1
 
 result = x * y;
 System.out.println(result); // 2
 
 result = x / y;
 System.out.println(result); // 2

 result = x % y;  
 System.out.println(result); // 0
 
 }
} 

