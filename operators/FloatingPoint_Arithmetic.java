public class FloatingPoint_Arithmetic {
    public static void main(String[] args) {
        System.out.println("\nFloating Point Arithmetic");
        double a = 1 + 1;
        double b = a * 3;
        double c = b / 4;
        double d = c - a;
        double e = -d;
        System.out.println("a = " + a); // 2.0
        System.out.println("b = " + b); // 6.0
        System.out.println("c = " + c); // 1.5
        System.out.println("d = " + d); // -0.5
        System.out.println("e = " + e); // 0.5
    }
}