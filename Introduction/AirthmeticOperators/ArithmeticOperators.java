public class ArithmeticOperators{
 public static void main(String[] args){
 int x = 2;
 int y = 1;
 int result = 0;
 
 result = x + y;
 System.out.println(result);
 
 result = x - y;
 System.out.println(result);
 
 result = x * y;
 System.out.println(result);
 
 result = x / y;
 System.out.println(result);
 result = x % y;
 System.out.println(result);
 
 }
} 